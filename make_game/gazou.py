from PIL import Image, ImageDraw

# ===============================
# プレイヤー画像（かっこいいスペースシップ）の生成
# ===============================

# 16x16 グリッドでデザイン（各文字が1ピクセル）
# '0' → 透明  
# '1' → 本体（シルバー）  
# '2' → コックピット（明るいブルー）  
spaceship_design = [
    "0000001100000000",
    "0000011110000000",
    "0000111111000000",
    "0001111111100000",
    "0011112111110000",
    "0011122211110000",
    "0111111111111100",
    "0111111111111100",
    "1111111111111110",
    "1111111111111110",
    "1111111111111110",
    "0111111111111100",
    "0011111111110000",
    "0001111111100000",
    "0000111111000000",
    "0000011110000000"
]

spaceship_colors = {
    '0': (0, 0, 0, 0),             # 透明
    '1': (192, 192, 192, 255),       # シルバー（本体）
    '2': (0, 191, 255, 255)          # 明るいブルー（コックピット）
}

# 各ピクセルを拡大する倍率（例：4 → 16×4 = 64 ピクセルサイズ）
scale = 4  
player_size = (16 * scale, 16 * scale)
player_img = Image.new("RGBA", player_size, (0, 0, 0, 0))
draw = ImageDraw.Draw(player_img)

for y, row in enumerate(spaceship_design):
    for x, char in enumerate(row):
        color = spaceship_colors.get(char, (0, 0, 0, 0))
        if color[3] != 0:  # 透明でなければ描画
            draw.rectangle(
                [x * scale, y * scale, x * scale + scale - 1, y * scale + scale - 1],
                fill=color
            )

player_img.save("player.png")
print("player.png を生成しました。")

